// Language detection and management
class LanguageManager {
    constructor() {
        this.currentLang = this.detectLanguage();
        this.init();
    }
    
    detectLanguage() {
        // Check localStorage first
        const savedLang = localStorage.getItem('cannabis-madrid-lang');
        if (savedLang && translations[savedLang]) {
            return savedLang;
        }
        
        // Check browser language
        const browserLang = navigator.language.split('-')[0];
        if (translations[browserLang]) {
            return browserLang;
        }
        
        // Default to English
        return 'en';
    }
    
    init() {
        this.updatePageLanguage();
        this.createLanguageSwitcher();
    }
    
    switchLanguage(lang) {
        if (translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('cannabis-madrid-lang', lang);
            this.updatePageLanguage();
            this.updateLanguageSwitcher();
        }
    }
    
    updatePageLanguage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.getTranslation(key);
            if (translation) {
                if (element.tagName === 'INPUT' && element.type === 'submit') {
                    element.value = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });
        
        // Update page title and meta description
        this.updateMetaTags();
    }
    
    getTranslation(key) {
        const keys = key.split('.');
        let translation = translations[this.currentLang];
        
        for (const k of keys) {
            if (translation && translation[k]) {
                translation = translation[k];
            } else {
                return null;
            }
        }
        
        return translation;
    }
    
    updateMetaTags() {
        // Update page title
        const titleElement = document.querySelector('title');
        if (titleElement) {
            const baseTitle = this.getTranslation('home.title') || 'Cannabis Madrid';
            titleElement.textContent = `${baseTitle} - Premium Dispensary`;
        }
        
        // Update meta description
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) {
            const desc = this.getTranslation('home.subtitle') || 'Premium cannabis products and expert guidance in Madrid';
            metaDesc.setAttribute('content', desc);
        }
    }
    
    createLanguageSwitcher() {
        const navbar = document.querySelector('.nav-container');
        if (navbar) {
            const languageSwitcher = document.createElement('div');
            languageSwitcher.className = 'language-switcher';
            
            // Create buttons for all available languages
            const languages = ['en', 'es', 'de'];
            const buttons = languages.map(lang => {
                const langInfo = translations[lang].lang;
                const isActive = this.currentLang === lang ? 'active' : '';
                return `<button class="lang-btn ${isActive}" data-lang="${lang}" title="${langInfo.name}">${langInfo.flag}</button>`;
            }).join('');
            
            languageSwitcher.innerHTML = buttons;
            
            // Insert before nav-toggle
            const navToggle = navbar.querySelector('.nav-toggle');
            navbar.insertBefore(languageSwitcher, navToggle);
            
            // Add event listeners
            languageSwitcher.addEventListener('click', (e) => {
                if (e.target.classList.contains('lang-btn')) {
                    const lang = e.target.getAttribute('data-lang');
                    this.switchLanguage(lang);
                }
            });
        }
    }
    
    updateLanguageSwitcher() {
        const langBtns = document.querySelectorAll('.lang-btn');
        langBtns.forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === this.currentLang) {
                btn.classList.add('active');
            }
        });
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.languageManager = new LanguageManager();
});
